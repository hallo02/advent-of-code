    static long getWeight(List<String> fields) {
        long sum = 0;
        for (int l = 0; l < fields.size(); l++) {
            for (int p = 0; p < fields.get(0).length(); p++) {
                if(fields.get(l).charAt(p) == 'O'){
                    sum += fields.size() - l;
                }
            }
        }
        return sum;
    }

    static void printField(List<String> field) {
        field.forEach(System.out::println);
    }

    static void moveUp(List<String> fields) {
        for (int l = 1; l < fields.size(); l++) {
            for (int p = 0; p < fields.get(0).length(); p++) {
                int lFrom = l;
                int lTo = lFrom - 1;
                while (lTo >= 0 && fields.get(lTo).charAt(p) == '.' && fields.get(lFrom).charAt(p) == 'O') {
                    // set old to .
                    StringBuilder sb = new StringBuilder(fields.get(lFrom));
                    sb.setCharAt(p, '.');
                    fields.set(lFrom, sb.toString());
                    // set above to 0
                    sb = new StringBuilder(fields.get(lTo));
                    sb.setCharAt(p, 'O');
                    fields.set(lTo, sb.toString());
                    lFrom--;
                    lTo--;
                }

            }
        }
    }

